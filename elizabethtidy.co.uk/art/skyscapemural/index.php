		<p>Having previously created a mountainscape mural to decorate my bedroom employing only the walls I wanted to use the ceiling as well to more faithfully create an illusion of space. I worked from a fish-eye and a rectilinear view of a cloudscape to produce a compromise image that roughly simulated a dome of sky. The stylized nature of the clouds was influenced in part by the <a href="http://en.wikipedia.org/wiki/Image:El_Greco_View_of_Toledo.jpg" title="Wikipedia: View of Toledo">dynamic skies</a> often found in the work of <a href="http://en.wikipedia.org/wiki/El_Greco" title="Wikipedia: El Greco">El Greco</a>.</p>
